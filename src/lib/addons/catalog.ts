// ============================================================================
// Add-on catalog — the single source of truth for what add-ons exist.
// ----------------------------------------------------------------------------
// New add-on? Add an entry here, ship code that reads it, done. No DB
// migration needed for new add-ons themselves; only their feature-specific
// tables (e.g. provider_qa for the qa add-on) require migrations.
//
// Pricing rules (founder direction 2026-06-07):
// - Free-forever add-ons: priceMonthlyIdr = 0 AND freeForever = true.
//   We never charge for these. They are the hook.
// - Paid add-ons: priceMonthlyIdr > 0. No surprise free trial — show the
//   price prominently from the first card the user sees. Optional explicit
//   trialDays for cases we want to let people try before paying.
// ============================================================================

export type AddonId = 'qa' | 'payments' | 'social-auto'

export type AddonBilling =
  | { kind: 'free-forever' }
  | { kind: 'paid'; priceMonthlyIdr: number; trialDays?: number }

export type Addon = {
  id:           AddonId
  /** Stable internal slug for the URL: /add-ons/<slug>. Matches id today. */
  slug:         string
  /** Lucide icon name. The storefront/dashboard imports this dynamically. */
  iconName:     'MessageCircleQuestion' | 'CreditCard' | 'Sparkles'
  label:        { id: string; en: string }
  /** One-liner that fits on a storefront card under the title. */
  tagline:      { id: string; en: string }
  /** Longer description for the detail page. Markdown-light (plain text + newlines). */
  description:  { id: string; en: string }
  billing:      AddonBilling
  /** Whether v1 is shippable today. Off-flagged add-ons appear on the
   *  storefront in a "Coming soon" state and can't be activated. */
  available:    boolean
}

