export function findAddon(id: string): Addon | null {
  return ADDONS.find((a) => a.id === id || a.slug === id) ?? null
}

export function formatIdr(n: number): string {
  return 'Rp ' + n.toLocaleString('id-ID')
}

/** Human-friendly price chip text per locale. */
export function priceLabel(addon: Addon, locale: 'id' | 'en'): string {
  if (addon.billing.kind === 'free-forever') {
    return locale === 'id' ? 'Gratis selamanya' : 'Free forever'
  }
  const monthly = formatIdr(addon.billing.priceMonthlyIdr)
  if (locale === 'id') {
    return addon.billing.trialDays
      ? `${monthly} / bulan · Coba gratis ${addon.billing.trialDays} hari`
      : `${monthly} / bulan`
  }
  return addon.billing.trialDays
    ? `${monthly} / month · ${addon.billing.trialDays}-day free trial`
    : `${monthly} / month`
}
