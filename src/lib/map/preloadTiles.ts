// Tile providers we mirror — must match the SW's TILE_HOSTS so the cache
// catches them. The first URL is preferred; the SW handles failover.
const TILE_URL_TEMPLATE = 'https://tiles.openfreemap.org/planet/{z}/{x}/{y}.pbf'

// Defaults — tunable per call.
const DEFAULT_ZOOM_MIN = 10
const DEFAULT_ZOOM_MAX = 14
const CONCURRENCY = 6
const MAX_TILES = 1200            // hard cap per call across all zooms
const FETCH_TIMEOUT_MS = 8_000    // skip tiles that hang — we'll get them on demand

function lonToTileX(lon: number, z: number): number {
  return Math.floor(((lon + 180) / 360) * Math.pow(2, z))
}

function latToTileY(lat: number, z: number): number {
  const rad = (lat * Math.PI) / 180
  return Math.floor(
    ((1 - Math.log(Math.tan(rad) + 1 / Math.cos(rad)) / Math.PI) / 2) * Math.pow(2, z),
  )
}

function buildTileUrls(bbox: [number, number, number, number], zmin: number, zmax: number): string[] {
  const [w, s, e, n] = bbox
  const urls: string[] = []
  for (let z = zmin; z <= zmax; z++) {
    const xMin = lonToTileX(w, z)
    const xMax = lonToTileX(e, z)
    // Note: tile Y is inverted (north → smaller Y), so we use n then s.
    const yMin = latToTileY(n, z)
    const yMax = latToTileY(s, z)
    for (let x = Math.min(xMin, xMax); x <= Math.max(xMin, xMax); x++) {
      for (let y = Math.min(yMin, yMax); y <= Math.max(yMin, yMax); y++) {
        urls.push(
          TILE_URL_TEMPLATE
            .replace('{z}', String(z))
            .replace('{x}', String(x))
            .replace('{y}', String(y)),
        )
        if (urls.length >= MAX_TILES) return urls
      }
    }
  }
  return urls
}

type NetworkInformationLike = {
  saveData?: boolean
  effectiveType?: 'slow-2g' | '2g' | '3g' | '4g'
}

function shouldSkipPreload(): boolean {
  if (typeof navigator === 'undefined') return true

  // Skip when PMTiles is the configured tile source — pmtiles serves via
  // HTTP Range requests against a single archive, not XYZ tile fetches,
  // so pre-warming the openfreemap XYZ URLs is pure waste (and floods
  // the dev console with CORS errors since openfreemap doesn't send
  // Access-Control-Allow-Origin). This honours the same env contract
  // resilientStyle.ts uses.
  if (process.env.NEXT_PUBLIC_PMTILES_URL) return true

  // Dev kill switch — silences the openfreemap CORS storm completely.
  if (process.env.NEXT_PUBLIC_MAPS_OFFLINE === 'true') return true

  // Honor Save-Data + slow-link hints. Indonesian users on prepaid plans
  // care more about MB used than instant tiles — respect that signal.
  const conn = (navigator as Navigator & { connection?: NetworkInformationLike }).connection
  if (!conn) return false
  if (conn.saveData) return true
  if (conn.effectiveType === '2g' || conn.effectiveType === 'slow-2g') return true
  return false
}

async function fetchWithTimeout(url: string, timeoutMs: number): Promise<void> {
  if (typeof AbortController === 'undefined') {
    await fetch(url, { cache: 'force-cache', mode: 'cors' }).catch(() => undefined)
    return
  }
  const ctrl = new AbortController()
  const t = setTimeout(() => ctrl.abort(), timeoutMs)
  try {
    await fetch(url, { cache: 'force-cache', mode: 'cors', signal: ctrl.signal })
  } catch {
    /* swallow — pre-warm is best-effort */
  } finally {
    clearTimeout(t)
  }
}

async function runConcurrent(urls: string[], concurrency: number): Promise<void> {
  let idx = 0
  async function worker() {
    while (idx < urls.length) {
      const myIdx = idx++
      await fetchWithTimeout(urls[myIdx], FETCH_TIMEOUT_MS)
    }
  }
  const workers: Promise<void>[] = []
  for (let i = 0; i < Math.min(concurrency, urls.length); i++) {
    workers.push(worker())
  }
  await Promise.all(workers)
}

/**
 * Pre-warm the Service Worker tile cache for a given city's bounding box.
 * Non-blocking — the caller can fire-and-forget. Skips entirely on
 * save-data or 2G connections.
 *
 * @param city  CityBbox to pre-warm (one of PRELOAD_CITIES, or custom)
 * @param opts  Optional zoom range overrides
 * @returns     Number of tiles attempted (0 if skipped)
 */
export async function preloadCityTiles(
  city: CityBbox,
  opts?: { zmin?: number; zmax?: number },
): Promise<number> {
  if (shouldSkipPreload()) return 0
  const zmin = opts?.zmin ?? DEFAULT_ZOOM_MIN
  const zmax = opts?.zmax ?? DEFAULT_ZOOM_MAX
  const urls = buildTileUrls(city.bbox, zmin, zmax)
  await runConcurrent(urls, CONCURRENCY)
  return urls.length
}

/**
 * Pre-warm tiles for an arbitrary bbox — not tied to PRELOAD_CITIES.
 * Used by /cari to warm the actual pickup → dropoff corridor for the
 * user's specific trip, including locations outside our 10 curated
 * cities. Bbox is auto-expanded by ~200m on each side so route
 * pan/zoom outside the strict bounds still hits cached tiles.
 *
 * Returns 0 when skipped (save-data, 2G, PMTiles configured, etc.) or
 * when the bbox is invalid.
 */
export async function preloadBbox(
  bbox: [number, number, number, number],
  opts?: { zmin?: number; zmax?: number; label?: string },
): Promise<number> {
  if (shouldSkipPreload()) return 0
  const [w, s, e, n] = bbox
  if (![w, s, e, n].every((v) => Number.isFinite(v))) return 0
  if (w >= e || s >= n) return 0
  // Pad bbox by ~200m on each side so the user's pinch-out and slight
  // pan after the route loads still hits cached tiles.
  const pad = 0.002 // ~220m at the equator
  const padded: [number, number, number, number] = [w - pad, s - pad, e + pad, n + pad]
  const zmin = opts?.zmin ?? DEFAULT_ZOOM_MIN
  const zmax = opts?.zmax ?? DEFAULT_ZOOM_MAX
  const urls = buildTileUrls(padded, zmin, zmax)
  await runConcurrent(urls, CONCURRENCY)
  return urls.length
}

/**
 * Compute a tight bbox containing both points + an optional buffer
 * (default 5km). Used to derive the warm region for /cari when both
 * pickup + dropoff are set.
 */
export function bboxFromPoints(
  a: { lat: number; lng: number },
  b: { lat: number; lng: number },
  bufferKm = 5,
): [number, number, number, number] {
  const minLat = Math.min(a.lat, b.lat)
  const maxLat = Math.max(a.lat, b.lat)
  const minLng = Math.min(a.lng, b.lng)
  const maxLng = Math.max(a.lng, b.lng)
  const latPad = bufferKm / 111
  // Lng pad shrinks with latitude (away from equator).
  const lngPad = bufferKm / (111 * Math.cos((minLat * Math.PI) / 180))
  return [minLng - lngPad, minLat - latPad, maxLng + lngPad, maxLat + latPad]
}

/**
 * Pick the nearest PRELOAD_CITIES entry to the given lat/lng. Returns
 * undefined if no city centroid is within ~150km (likely the user is
 * outside our primary markets).
 */
export function nearestPreloadCity(lat: number, lng: number): CityBbox | undefined {
  let best: { city: CityBbox; d: number } | undefined
  for (const city of PRELOAD_CITIES) {
    const cy = (city.bbox[1] + city.bbox[3]) / 2
    const cx = (city.bbox[0] + city.bbox[2]) / 2
    const dy = (lat - cy) * 111
    const dx = (lng - cx) * 111 * Math.cos((lat * Math.PI) / 180)
    const d = Math.hypot(dx, dy)
    if (!best || d < best.d) best = { city, d }
  }
  if (!best) return undefined
  return best.d < 150 ? best.city : undefined
}
