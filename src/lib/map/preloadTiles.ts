// ============================================================================
// Background tile pre-warmer for Indonesian cities.
// ----------------------------------------------------------------------------
// Phase 3 of the tile-resilience layer. Walks a city's bounding box at
// the zoom levels customers actually use (city overview → neighbourhood),
// and fires fetch() for each tile. The Service Worker (Phase 1) catches
// each response and tucks it into the cache, so the next time the user
// opens the app or pans to that area they get instant tiles even with
// zero signal.
//
// COST CONTROL:
//   • Capped at MAX_TILES per call to avoid runaway prefetch
//   • Cooperative concurrency (CONCURRENCY parallel fetches)
//   • Each fetch uses { cache: 'force-cache' } so the browser HTTP cache
//     also short-circuits if it already has the tile
//   • Connection-type aware: skips entirely on save-data / cellular when
//     navigator.connection signals slow links
//
// TILE MATH:
//   For zooms Z = 10..14 over a city bbox:
//     z=10: 1-4 tiles
//     z=11: 4-9 tiles
//     z=12: 16-36 tiles
//     z=13: 64-144 tiles
//     z=14: 256-576 tiles
//   Total per city: 300-700 tiles. A medium city like Yogyakarta is ~450.
//   At 25-50KB per vector tile, that's ~15-25 MB per city — well inside
//   the SW cache budget set in public/sw.js (TILE_LIMIT = 1500).
// ============================================================================

export type CityBbox = {
  slug: string
  name: string
  // [west, south, east, north] in degrees — same convention as MapLibre.
  bbox: [number, number, number, number]
}

